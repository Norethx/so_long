#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	while (i >= 0)
	{
		if (((unsigned char *)s)[i] == (unsigned char) c)
			return (&s[i]);
		i--;
	}
	return (((void *)0));
}
