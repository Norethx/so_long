#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	int	i;
	int	len;

	len = ft_strlen(s);
	i = 0;
	while (i <= len)
	{
		if (((unsigned char *)s)[i] == (unsigned char) c)
			return (&s[i]);
		i++;
	}
	return (((void *)0));
}
